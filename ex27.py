def peak(A):
    l = 0
    r = len(A) - 1

    while l < r:
        m = (l + r) // 2

        if A[m] < A[m + 1]:
            l = m + 1
        else:
            r = m

    return l + 1


tests = [
    [1],
    [1, 3],
    [3, 1],
    [1, 5, 2],
    [1, 3, 7, 4, 2],
    [2, 6, 9, 12, 8, 5, 1],
    [4, 10, 15, 20, 18, 11, 3],
    [-5, -2, 0, 4, 9, 3, -1],
    [10, 20, 30, 40, 50, 25, 5],
    [1, 2, 3, 4, 5, 6, 7],
    [7, 6, 5, 4, 3, 2, 1],
]

for A in tests:
    p = peak(A)
    print(A, "пик -", A[p - 1])
